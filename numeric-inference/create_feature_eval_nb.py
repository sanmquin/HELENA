                    "    coeffs = next(c for c in eval_dataset if c['channel_id'] == cid)['model']['coefficients']\n",
                    "    \n",
                    "    for res in channel_res['results']:\n",
                    "        emb = res['reduced_embedding']\n",
                    "        correct_labels = 0\n",
                    "        total_dims = len(res['dimension_evaluations'])\n",
                    "        \n",
                    "        for dim_str, pred_label in res['dimension_evaluations'].items():\n",
                    "            dim_idx = int(dim_str)\n",
                    "            contribution = coeffs[dim_idx] * emb[dim_idx]\n",
                    "            gt_label = get_ground_truth_label(contribution, thresholds[dim_idx])\n",
                    "            \n",
                    "            if str(pred_label).lower() == gt_label:\n",
                    "                correct_labels += 1\n",
                    "        \n",
                    "        label_acc = correct_labels / total_dims\n",
                    "        pred_error = abs(res['predicted_log_views'] - res['actual_log_views'])\n",
                    "        \n",
                    "        analysis_rows.append({\n",
                    "            'channel': cname,\n",
